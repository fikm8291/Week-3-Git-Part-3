# Step 1: Python Task
# -------------------
# Your task is to enhance this simple program:
print("Welcome to the Greeting Program!")

# TODO:
# 1. Ask the user for their name
# 2. Ask the user for their favourite hobby
# 3. Print a greeting using both inputs

# Step 2: Git Branch
# ------------------
# 1. Check the current branch:
#    git branch
# 2. Create a new branch using your name or initials:
#    git branch yourname
# 3. Switch to your branch:
#    git checkout yourname
# 4. Make your Python changes (input/output for name & hobby)
# 5. Stage and commit your changes:
#    git add Ex3_git_branch.py
#    git commit -m "Added greeting with hobby"
# 6. Push your branch to GitHub:
#    git push origin yourbranchname

# Step 3: Pull Request
# --------------------
# 1. Go to the GitHub repository in your browser
# 2. Click "Compare & pull request" for your branch
# 3. Add a clear description of what you changed
# 4. Submit the pull request for review
# 5. Your tutor or classmates can merge your changes after review

# Step 4: Done!
# --------------
# Once your pull request is merged, switch back to main and pull the latest changes:
# git checkout main
# git pull origin main
